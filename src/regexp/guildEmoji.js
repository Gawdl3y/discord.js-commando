exports.RegExp = {
	full: () => /<(a?):([^\s\\]+):([0-9]{7,})>/,
	fullID: () => /<a?:[^\s\\]+:([0-9]{7,})>/,
	colons: () => /:([^\s\\]+):/,
	colonsOnly: () => /(?<!<a?):([^\s\\]+?):(?!\d+>)/
};

exports.RegExpSingle = {
	full: () => /^<(a?):([^\s\\]+):([0-9]{7,})$>/,
	fullID: () => /^<a?:[^\s\\]+:([0-9]{7,})>$/,
	colons: () => /^:([^\s\\]+):$/,
	colonsOnly: () => /^(?<!<a?):([^\s\\]+?):(?!\d+>)$/
};
